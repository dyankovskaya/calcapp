package ru.dyankovskaya.calcapp.util;

public class Calculator  {

    public static int sum(int x, int y) {
        int result = x + y;
        return result;
    }

    public static int subtract(int x, int y) {
        int result = x - y;
        return result;
    }

    public static int multiply(int x, int y) {
        int result = x * y;
        return result;
    }
}
